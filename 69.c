#include <stdio.h>
#include <stdlib.h>
#include<windows.h>
int main()
{
	char a[40][40]={"###############################",
		 "#O         #   ##    # ### ####",
		 "# ###### # # #    #  # ### ####",
		 "# #   ## #   # ####  # ###   ##",
		 "#   # ##  ###    #   #  ## ####",
		 "##### #     #  ####  #     ####",
		 "#   # ##### #  ####  # # #    #",
		 "# # #    ## #   ## ### # # ####",
		 "# # # ##    #   ## #   # # ####",
		 "# # # #######   ## #######   ##",
		 "# #   ##   # ##   ###### ##   #",
		 "# ###### # #####    #       # #",
		 "#            # #   ##### ## # o",
		 "# ######## #####   ##### ## # #",
		 "#     # ## #####   ###      ###",
		 "##### # ## #        ####### # #",
		 "#     # ## ## ###         #   #",
		 "# # ###       ###### #######  #",
		 "# #    ### ##      #          #",
		 "###############################"},ch;
int i,x,y,p,q;
x=1;y=1;p=12;q=29;
for(i=0;i<=29;i++)
puts(a[i]);
while(x!=p||y!=q)
{
ch=getch();
if(ch=='s')
{
if(a[x+1][y]!='#')
{
a[x][y]=' ';
x++;
a[x][y]='O';
}}
if(ch=='w')
{
if(a[x-1][y]!='#')
{
a[x][y]=' ';
x--;
a[x][y]='O';
}}
if(ch=='a')
{
if(a[x][y-1]!='#')
{
a[x][y]=' ';
y--;
a[x][y]='O';
}}
if(ch=='d')
{
if(a[x][y+1]!='#')
{
a[x][y]=' ';
y++;
a[x][y]='O';
}}
system("cls");
for(i=0;i<=29;i++)
puts(a[i]);
}
printf("你赢了（到出口了）");
Sleep(10000);
	return 0;
}

